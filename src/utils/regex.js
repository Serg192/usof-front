const regex = {
  USER_LOGIN_REGEX: /^[a-zA-Z0-9-_]{4,40}$/,
  USER_PASSWORD_REGEX:
    /^(?=.*[A-Z].*[A-Z].*[A-Z])(?=.*[!@#$&*])(?=.*[0-9].*[0-9].*[0-9]).{8,}$/,
  EMAIL_REGEX:
    /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  USER_FULL_NAME: /^[a-zA-Z][a-zA-Z\s]{0,49}$/,
};

module.exports = regex;
